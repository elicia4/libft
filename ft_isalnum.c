#include "libft.h"

int	ft_isalnum(int _char)
{
	if ((_char >= 48) && (_char <= 57))
		return (1);
	else if (((_char >= 65) && (_char <= 90))
		|| ((_char >= 97) && (_char <= 122)))
		return (1);
	else
		return (0);
}
