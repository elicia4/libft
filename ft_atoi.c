#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			res;
	int			sign;
	const char	*str;

	sign = 1;
	res = 0;
	str = nptr;
	while (*str == ' ' || *str == '\n' || *str == '\t'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*str++ - '0');
	return (sign * res);
}
